                'Parent', hParent,...
                'Units', 'pixels',...
                'Title', 'Process Tool',...
                'Clipping', 'on',...
                'Position', Utils.lt2lb([dLeft dTop this.dWidth this.dHeight], hParent) ...
            );
			drawnow;
            
            dPadX = 10;
            dWidthName = 120;
            dWidthThick = 55;
            dWidthTemp = 55;
            dWidthTime = 55;
                     
            dTop = 20;
            dSep = 55;

            

            % Build filter Hz, Volts scale and time step

            
            this.uieUser.build( ...
                this.hPanel, ...
                dPadX, ...
                dTop, ...
                dWidthName, ...
                Utils.dEDITHEIGHT ...
            );
        
            dTop = dTop + dSep;
            
            this.uieUL1Name.build( ...
                this.hPanel, ...
                dPadX, ...
                dTop, ...
                dWidthName, ...
                Utils.dEDITHEIGHT ...
            );
        
            this.uieUL1Thick.build( ...
                this.hPanel, ...
                2*dPadX + dWidthName, ...
                dTop, ...
                dWidthThick, ...
                Utils.dEDITHEIGHT ...
            );
        
            this.uieUL1PABTemp.build( ...
                this.hPanel, ...
                3*dPadX + dWidthName + dWidthThick, ...
                dTop, ...
                dWidthTemp, ...
                Utils.dEDITHEIGHT ...
            );
        
            this.uieUL1PABTime.build( ...
                this.hPanel, ...
                4*dPadX + dWidthName + dWidthThick + dWidthTemp, ...
                dTop, ...
                dWidthTime, ...
                Utils.dEDITHEIGHT ...
            );
        
        
        
            dTop = dTop + dSep;
            
            this.uieUL2Name.build( ...
                this.hPanel, ...
                dPadX, ...
                dTop, ...
                dWidthName, ...
                Utils.dEDITHEIGHT ...
            );
        
            this.uieUL2Thick.build( ...
                this.hPanel, ...
                2*dPadX + dWidthName, ...
                dTop, ...
                dWidthThick, ...
                Utils.dEDITHEIGHT ...
            );
        
            this.uieUL2PABTemp.build( ...
                this.hPanel, ...
                3*dPadX + dWidthName + dWidthThick, ...
                dTop, ...
                dWidthTemp, ...
                Utils.dEDITHEIGHT ...
            );
        
            this.uieUL2PABTime.build( ...
                this.hPanel, ...
                4*dPadX + dWidthName + dWidthThick + dWidthTemp, ...
                dTop, ...
                dWidthTime, ...
                Utils.dEDITHEIGHT ...
            );
        
            dTop = dTop + dSep;
            
            this.uieResistName.build( ...
                this.hPanel, ...
                dPadX, ...
                dTop, ...
                dWidthName, ...
                Utils.dEDITHEIGHT ...
            );
        
            this.uieResistThick.build( ...
                this.hPanel, ...
                2*dPadX + dWidthName, ...
                dTop, ...
                dWidthThick, ...
                Utils.dEDITHEIGHT ...
            );
        
            this.uieResistPABTemp.build( ...
                this.hPanel, ...
                3*dPadX + dWidthName + dWidthThick, ...
                dTop, ...
                dWidthTemp, ...
                Utils.dEDITHEIGHT ...
            );
        
            this.uieResistPABTime.build( ...
                this.hPanel, ...
                4*dPadX + dWidthName + dWidthThick + dWidthTemp, ...
                dTop, ...
                dWidthTime, ...
                Utils.dEDITHEIGHT ...
            );
        
            
        
        
            dTop = dTop + dSep;
            
            
            this.uieResistPEBTemp.build( ...
                this.hPanel, ...
                dPadX, ...
                dTop, ...
                dWidthTemp, ...
                Utils.dEDITHEIGHT ...
